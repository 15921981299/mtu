export type MtuPart = {
  slug: string;
  partNumber: string;
  name: string;
  series: string[];
  category: string;
  summary: string;
  image: string;
  availability: string;
  applications: string[];
  notes: string[];
};

const partImage = '/images/engine-parts-sensors-catalog.png';

const slugifyPart = (value: string) =>
  value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');

const createCatalogPart = (part: {
  partNumber: string;
  name: string;
  series: string[];
  category: string;
}): MtuPart => ({
  slug: `${slugifyPart(part.partNumber)}-${slugifyPart(part.name)}`,
  partNumber: part.partNumber,
  name: part.name,
  series: part.series,
  category: part.category,
  summary: `${part.name} inquiry support for ${part.series.join(' / ')} engines with part-number verification before quotation.`,
  image: partImage,
  availability: 'Stock, replacement status, lead time, and shipping route are confirmed after inquiry.',
  applications: ['Marine service', 'Power generation', 'Industrial engine maintenance'],
  notes: [
    'Confirm engine model, serial number, required quantity, and destination before ordering.',
    'Send old part photos or nameplate details when markings or replacement status are unclear.',
  ],
});

