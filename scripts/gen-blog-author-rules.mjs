/**
 * Generate blog-author-rules.ts from indexable posts + tag priority.
 * Run: node scripts/gen-blog-author-rules.mjs
 */
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const audit = JSON.parse(fs.readFileSync(path.join(root, 'docs/indexable-blog-audit.json'), 'utf8'));
const blogSrc = fs.readFileSync(path.join(root, 'src/data/blog.ts'), 'utf8');
const slugs = audit.teamAuthorIndexable;

const tagPriority = [
  'sourcing',
  'quality',
  'industries',
  'tolerances',
  'design',
  'process',
  'materials',
  'cost',
  'prototyping',
];
const lisaTags = new Set(['sourcing', 'quality', 'industries', 'materials']);

function getTags(slug) {
  const re = new RegExp(`slug: '${slug.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}'[\\s\\S]*?tags: \\[([^\\]]+)\\]`);
  const m = blogSrc.match(re);
  if (!m) return [];
  return [...m[1].matchAll(/'([^']+)'/g)].map((x) => x[1]);
}

function pickAuthor(tags) {
  for (const tag of tagPriority) {
    if (tags.includes(tag)) return lisaTags.has(tag) ? 'lisa-huang' : 'wei-chen';
  }
  return 'wei-chen';
}

const indexableAuthorBySlug = {};
for (const slug of slugs) {
  indexableAuthorBySlug[slug] = pickAuthor(getTags(slug));
}

const lisaCount = Object.values(indexableAuthorBySlug).filter((a) => a === 'lisa-huang').length;

const out = `/**
 * Explicit author assignments for indexable posts outside pillar auto-mapping.
 * Generated by scripts/gen-blog-author-rules.mjs — re-run after blog tag changes.
 */
import type { AuthorSlug } from './blog-authors';

export const indexableAuthorBySlug: Partial<Record<string, AuthorSlug>> = ${JSON.stringify(indexableAuthorBySlug, null, 2).replace(/"/g, "'")};
`;

fs.writeFileSync(path.join(root, 'src/data/blog-author-rules.ts'), out);
console.log(`Wrote blog-author-rules.ts — ${slugs.length} slugs (${lisaCount} lisa-huang, ${slugs.length - lisaCount} wei-chen)`);
